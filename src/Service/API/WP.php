    public function __call($functionName, array $args = array())
    {
        $numArgs = count($args);

        if($numArgs < 1) {
            return $functionName();
        } else if($numArgs === 1) {
            return $functionName($args[0]);
        } else if($numArgs === 2) {
            return $functionName($args[0], $args[1]);
        } else if($numArgs === 3) {
